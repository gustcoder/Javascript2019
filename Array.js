
function ArrayFirst(array) {
    return array[0]
}    
function ArrayLast(array) {
    return array[array.length-1]
}


let instrumentos = ["Guitarra","Bateria","Baixo"]
console.log(instrumentos)
console.log(instrumentos.length)
console.log(ArrayFirst(instrumentos))
console.log(ArrayLast(instrumentos))